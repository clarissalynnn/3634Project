package com.example.a3634project;

public class FruitListItem {
    private int mFruitImage;
    private String mFruitName;

    public FruitListItem(int fruitImage, String fruitName){
        mFruitImage = fruitImage;
        mFruitName = fruitName;
    }
    public int getmFruitImage() {
        return mFruitImage;
    }

    public String getmFruitName() {
        return mFruitName;
    }

}
